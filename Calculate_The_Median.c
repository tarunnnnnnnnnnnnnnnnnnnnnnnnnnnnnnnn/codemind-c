#include<stdio.h>
int main( )
{
   int i,j,n,m,a[10],t,h=0;
   scanf("%d",&n);
   for(i=0;i<n;i++)
   {
       scanf("%d",&a[i]);
   }
   for(i=1;i<n;i++)
   {
      for(j=0;j<n-i;j++)
      {
         if(a[j]>a[j+1]) 
         {
            t=a[j];
            a[j]=a[j+1];
            a[j+1]=t;
         }
      }
   }
   if(n%2!=0)
   {
      h=(n+1)/2;
      m=a[h-1];
   }
   else
   {
       h=(n/2);
       m=a[h];
   }
printf("%d",m);
}
